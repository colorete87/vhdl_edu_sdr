function y = raised_cosine (t, Tsymb, Beta)
%-----------------------------------------------------------

  %---------------------------------------
  y = 1/Tsymb*sinc(t/Tsymb).*cos(pi*Beta*t/Tsymb)./(1-(2*Beta*t/Tsymb).^2);
  y(t==Tsymb/2/Beta) = pi/4/Tsymb*sinc(1/2/Beta);
  y(t==-Tsymb/2/Beta) = pi/4/Tsymb*sinc(1/2/Beta);
  %---------------------------------------

end
